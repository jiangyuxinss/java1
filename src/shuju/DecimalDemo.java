package shuju;

public class DecimalDemo {
    public static void main(String[] args) {
        //小数常量(默认是double类型的)
        System.out.println(3.14);//double类型的常量
        System.out.println(3.14F);//float类型的常量  加常量
        //小数变量
        //小数类型 变量名=小数常量；
        double pi=3.14;
        float pi2=3.18F;  //float 类型小数后必须要加


    }
}
