package shuju;

public class VarDemo {


     static int b=50;


    public static void main(String[] args) {
        /*变量的定义格式
         * 数据类型  变量名
         * 如： int age;
         *
         * */
        int age = 17;
        System.out.println(age);
        age = 18;
        System.out.println(age);
       /* int age=19; 不能同时定义相同变量*/
        System.out.println(b);
    }
}
